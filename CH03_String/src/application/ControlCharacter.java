package application;

public class ControlCharacter {

	public static void main(String[] args) {
		// 제어 문자 : \n \t
		System.out.println("======================");
		System.out.println("\t탭\t탭 띄웁니다.");
		System.out.println("======================");
		System.out.println("자바를 \n배우고\n 있습니다");
		System.out.println("======================");

		System.out.print("이것은");
		System.out.print("줄을");
		System.out.print("안내립니다.");
	}

}
